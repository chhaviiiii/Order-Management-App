package model;

// Represents the types that a product can have in the system.
// Only these predefined types can be used to categorize products.
public enum ProductType {
    CLOTHES,       // Represents products of the type "Clothes"
    ELECTRONICS,   // Represents products of the type "Electronics"
    FOOD,          // Represents products of the type "Food"
    MAKEUP         // Represents products of the type "Makeup"
}